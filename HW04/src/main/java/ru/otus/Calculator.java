package ru.otus;

public class Calculator {
    public static long sum (int x, int y){
        return x + y;
    }


    public static int minus (int x, int y){
        return x - y;
    }

    public static long multiply(int x, int y){
        return x * y;
    }

    public static int divide(int x, int y){
        return x / y;
    }
}
